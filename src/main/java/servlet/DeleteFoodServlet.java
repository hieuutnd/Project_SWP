package servlet;

import DAO.DAO;
import jakarta.servlet.RequestDispatcher;
import java.io.IOException;
import java.io.PrintWriter;
import jakarta.servlet.ServletException;
import jakarta.servlet.annotation.WebServlet;
import jakarta.servlet.http.HttpServlet;
import jakarta.servlet.http.HttpServletRequest;
import jakarta.servlet.http.HttpServletResponse;

/**
 *
 * @author Admin
 */
@WebServlet(name = "deletefood", urlPatterns = {"/DeleteFoodServlet"})
public class DeleteFoodServlet extends HttpServlet {

    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("text/html;charset=UTF-8");
        int foodID = Integer.parseInt(request.getParameter("FOODID"));
        DAO dao = new DAO();
        dao.deleteFood(foodID);
        request.getRequestDispatcher("listFood").forward(request, response);
        RequestDispatcher rd = request.getRequestDispatcher("./AdminPage/listStaff.jsp");
        rd.forward(request, response);
    }


    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
    }


    @Override
    public String getServletInfo() {
        return "Short description";
    }// </editor-fold>

}
